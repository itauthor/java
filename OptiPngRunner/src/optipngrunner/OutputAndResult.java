package optipngrunner;

public class OutputAndResult {
	String output;
	int	result;
	
	public OutputAndResult (String o, int r){
		output = o;
		result = r;
	}

	/**
	 * @return the output
	 */
	public final String getOutput() {
		return output;
	}

	/**
	 * @return the result
	 */
	public final int getResult() {
		return result;
	}
	

}
